// Number data type 
console.log("Basic Number data type");

let x = 10;
let y = 5;

console.log("x =", x);
console.log("y =", y);

console.log("x + y = ", x + y);
console.log("x - y = ", x - y);
console.log("x * y = ", x * y);
console.log("x / y = ", x / y);
console.log("x ** y = ", x ** y);

// Increment & Decrement
console.log("Increment & Decrement");

let a = 5;
console.log(a++);
/* output: 5 */
console.log(a);
/* output: 6 */

let b = 5;
console.log(++b);
/* output: 6 */
console.log(++b);
/* output: 7 */